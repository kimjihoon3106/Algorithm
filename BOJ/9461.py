n = int(input())
result = []

# 1, - 1
# 1, - 2
# 1, - 3
# (1+1) = 2, - 4
# 2 = 2, - 5

# (2+1) = 3, # 1 - 6
# (3+1) = 4, # 2 = #1 + 1 - 7
# (4+1) = 5, # 3 = #2 + 1 - 8

# (5+2) = 7, # 4 = #3 + 2 - 9
# (7+2) = 9, # 5 = #4 + 2 - 10

# (9+3) = 12, # 6 = #5 + #1 - 11
# (12+4) = 16, # 7 = #6 + #2 - 12
# (16+5) = 21, # 8 = #7 + #3 - 13
# (21+7) = 28, # 9 = #8 + #4 - 14
# (28+9) = 37, # 10 = #9 + #5 - 15
# (37+12) = 49, # 11 = #10 + #6 - 16
# (49+16) = 65, # 12 = #11 + #7 - 17
# (65+21) = 86, # 13 = #12 + #8 - 18
# (86+28) = 114 # 14 = #13 + #9 - 19

for i in range(n):
    num = int(input())

    lst = [
    1,1,1,
    2,2,3,
    4,5,7,
    9
    ]

    if num <= 10:
        result.append(lst[num-1])
    else:
        for j in range(10, num):
            lst.append(lst[j - 1] + lst[j - 5])

        result.append(lst[num-1])
    
print('\n'.join(map(str, result)))

            

